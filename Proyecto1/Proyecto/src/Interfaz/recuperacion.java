    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        recuperacionCorreo cor = new recuperacionCorreo();
        Properties prop = new Properties();
        Session ses = Session.getDefaultInstance(prop);
        MimeMessage mail = new MimeMessage(ses);
        SQL sql = new SQL();
        cor.setCorreo(correo.getText());
        int codigo = sql.recibirCodigo(cor);

        if (codigo != 0) {

            prop.setProperty("mail.smtp.host", "smtp.gmail.com");
            prop.setProperty("mail.smtp.starttls.enable", "true");
            prop.setProperty("mail.smtp.port", "587");
            prop.setProperty("mail.smtp.auth", "true");

            String correoEnvio = "rodripereira2625@gmail.com";
            String contrasena = "26280259rodrigo";
            String enviar = cor.getCorreo();
            String asunto = "Recuperación";
            String mensaje = "Código de recuperación " + codigo;

            try {
                mail.setFrom(new InternetAddress(correoEnvio));
                mail.addRecipient(Message.RecipientType.TO, new InternetAddress(enviar));
                mail.setSubject(asunto);
                mail.setText(mensaje);

                Transport transporte = ses.getTransport("smtp");
                transporte.connect(correoEnvio, contrasena);
                transporte.sendMessage(mail, mail.getRecipients(Message.RecipientType.TO));
                transporte.close();
                JOptionPane.showMessageDialog(null, "Se envió un correo de recuperación a " + cor.getCorreo());

            } catch (AddressException ex) {
                Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
                ex.printStackTrace();
            } catch (MessagingException ex) {
                Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

