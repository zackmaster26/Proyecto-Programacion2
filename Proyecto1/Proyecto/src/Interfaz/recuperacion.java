    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed

        if (!codigo4.equals("")) {
            cor.setCodigo(Integer.parseInt(codigo4.getText()));
        }
        cor.setCorreo(correo.getText());
        int codigo1 = sql.verifiarCodigo(cor);

        if (!correo.getText().equals("")) {
            if (codigo1 == 1) {
                if (!pass1.getPassword().equals("") && !pass2.getPassword().equals("")) {

                    String pass = new String(pass1.getPassword());
                    String repetir = new String(pass2.getPassword());

                    if (pass.equals(repetir)) {

                        if (pass.length() >= 6) {

                            String contra = CifradoContraseña.sha1(pass);
                            sql.updateContraseña(contra, cor);
                            JOptionPane.showMessageDialog(null, "Contraseña cambiada con éxito");
                            this.dispose();

                            try {
                                Thread.sleep(500);
                                Login lol = new Login();
                                lol.setVisible(true);
                                lol.requestFocus();
                            } catch (InterruptedException ex) {
                                Logger.getLogger(recuperacion.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Contraseña insegura");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "El código ingresado no es correcto");
            }

        } else {
            JOptionPane.showMessageDialog(null, "No ingresó ningún correo");
        }
    }//GEN-LAST:event_confirmarActionPerformed

